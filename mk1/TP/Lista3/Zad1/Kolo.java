class Kolo extends Figura{

    public float promien;

    Kolo(float promien){
        this.promien = promien;
    }

    @Override
    public float Pole(){
        return (float) 3.14159*promien*promien;
    }

    @Override
    public float Obwod(){
        return (float) 3.14159*2*promien;
    }
    
    @Override
    public String Nazwa(){
        return "Ko≈Ço";
    }
}